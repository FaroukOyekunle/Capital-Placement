namespace ConsoleProject.Enums
{
    // Gender enumeration represents different gender options.
    public enum Gender
    {
        // Male represents the male gender option and has a value of 1.
        Male = 1,

        // Female represents the female gender option and has a value of 2.
        Female,

        // RatherNotSay represents the option when a user prefers not to disclose their gender and has a value of 3.
        RatherNotSay,

        // Other represents other gender options that are not explicitly listed and has a value of 4.
        Other,
    }
}
