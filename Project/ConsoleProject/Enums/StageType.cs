namespace ConsoleProject.Enums
{
    // StageType enumeration represents different stages in an application process.
    public enum StageType
    {
        // Applied stage represents the initial stage when an applicant has applied for a program.
        Applied = 1,

        // VideoInterview stage represents the stage where applicants go through a video interview.
        VideoInterview,

        // Offered stage represents the stage when an offer is extended to the applicant.
        Offered,

        // Shortlisting stage represents the stage where applicants are shortlisted based on their qualifications.
        Shortlisting
    }
}
